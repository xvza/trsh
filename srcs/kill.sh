#!/usr/bin/env bash
###############################################################################
declare   "id=$(xprop -root | awk '/_NET_ACTIVE_WINDOW\(WINDOW\)/{print $NF}')"
declare   "pid=$(xprop -id ${id} | awk '/_NET_WM_PID\(CARDINAL\)/{print $NF}')"
###############################################################################
eval      "kill -${1?} ${pid}"
###############################################################################

#!/usr/bin/env bash
###########################################################
declare     "IFS=/"
###########################################################
while read item
do
  eval    "read -a item <<< ${item}"
  eval    "sed -i s/${item[0]}/${item[1]}/g ${1?}"
  echo    "${1?}: replace \"${item[0]}\" by \"${item[1]}\""
done   <  "${2?}"
###########################################################

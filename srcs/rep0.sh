#!/usr/bin/env bash
#################################################################
declare     "IFS=/"
#################################################################
while read item
do
  declare   "bin=$(read -a item <<< ${item})"
  declare   "str=${1?}: replace \"${item[0]}\" by \"${item[1]}\""
  declare   "bin=$(sed -i s/${item[0]}/${item[1]}/g ${1?})"
  echo      "${str}"
done    <   "${2?}"
